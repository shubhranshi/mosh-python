print(10 + 3)
print(10 - 3)
print(10 * 3)
print(10 / 3)   #complete division, gives the float value
print(10 // 3)  #integer division, gives just the integer part
print(10 % 3)   #modulo: gives remainder
print(10 ** 3)  #exponent: 10^3

#augmented assignment
x = 10
x = x + 3
x += 3
print(x)

